# Favorite Fruit: Make a list of your favorite fruits, and then write a
# series of independent if statements that check for certain fruits in
# your list.
# • Make a list of your three favorite fruits and call it
# favorite_fruits.
# • Write five if statements . Each should check whether a certain kind
# of fruit is in your list. If the fruit is in your list, the if block
# should print a statement, such as You really like bananas!

favorite_fruits = ['bananas', 'apples', 'pineapples']

if 'bananas' in favorite_fruits:
    print("Your really like bananas!")
if 'cherries' in favorite_fruits:
    print("Your really like cherries!")
if 'apples' in favorite_fruits:
    print("Your really like apples!")
if 'lemons' in favorite_fruits:
    print("Your really like lemons!")
if 'pineapples' in favorite_fruits:
    print("Your really like pineapples!")